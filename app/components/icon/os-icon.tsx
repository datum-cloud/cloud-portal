function normalizeOs(os?: string): string {
  if (!os) return '';
  const lower = os.toLowerCase();
  if (lower.includes('windows') || lower === 'win32' || lower === 'win64') return 'windows';
  if (lower.includes('mac') || lower.includes('darwin') || lower === 'macos') return 'macos';
  if (lower.includes('linux')) return 'linux';
  if (lower.includes('android')) return 'android';
  if (lower.includes('ios') || lower.includes('iphone') || lower.includes('ipad')) return 'ios';
  return lower;
}

