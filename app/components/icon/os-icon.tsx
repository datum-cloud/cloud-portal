const osLabelMap: Record<string, string> = {
  windows: 'Windows',
  macos: 'macOS',
  ios: 'iOS',
  darwin: 'macOS',
  linux: 'Linux',
  android: 'Android',
};

export function getOsLabel(os?: string): string {
  const normalized = normalizeOs(os);
  return osLabelMap[normalized] ?? os ?? 'Unknown';
}

export function OsIcon({ os, size = 16, className, ...props }: OsIconProps) {
  const normalized = normalizeOs(os);
  const IconComponent = osIconMap[normalized] ?? DefaultDeviceIcon;

  return <IconComponent width={size} height={size} className={className} {...props} />;
}
